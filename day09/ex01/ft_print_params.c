#include <unistd.h>

int	main(int argc, char **argv)
{
	int i;
	int j;

	(void)argc;
	(void)argv;
	j = 1;
	i = 0;
	while (j < argc)
	{
		while (argv[j][i] != '\0')
		{
			write(1, &argv[j][i], 1);
			i++;
		}
		write(1, "\n", 1);
		i = 0;
		j++;
	}
}
