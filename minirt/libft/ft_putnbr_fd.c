#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int temp;

	if (n == -2147483648)
		ft_putstr_fd("-2147483648", fd);
	else if (n < 0)
	{
		ft_putchar_fd('-', fd);
		ft_putnbr_fd(-n, fd);
	}
	else if (n < 10)
		ft_putchar_fd(n + 48, fd);
	else
	{
		temp = n % 10 + 48;
		ft_putnbr_fd(n / 10, fd);
		ft_putchar_fd(temp, fd);
	}
}
