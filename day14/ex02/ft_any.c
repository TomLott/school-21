int	ft_any(char **tab, int (*f)(char*))
{
	int i;
	int res;

	i = 0;
	res = 0;
	while (tab[i] != 0)
	{
		if ((f(tab[i])) == 1)
			res = 1;
		i++;
	}
	return (res);
}
