int	ft_find_next_prime(int nb)
{
	int i;
	int k;

	i = 0;
	k = 0;
	if (nb < 0)
	{
		return (2);
	}
	while (k <= 2147483647 || (nb + i) < 214483647)
	{
		k = 2;
		while ((nb + i) % k && (nb + i) != k)
		{
			k++;
		}
		if ((nb + i) == k)
		{
			return (k);
		}
		i++;
	}
	return (k);
}
