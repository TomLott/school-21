int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	int				x;

	i = 0;
	x = 0;
	while (i < n)
	{
		if (*s1 != *s2)
		{
			x = *s1 - *s2;
			return (x);
		}
		i++;
		s1++;
		s2++;
	}
	return (x);
}
