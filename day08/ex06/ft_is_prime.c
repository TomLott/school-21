int	ft_is_prime(int nb)
{
	int k;
	int i;

	k = 2;
	if (nb < 0)
	{
		return (0);
	}
	if (nb == 1 || nb == 0)
	{
		return (1);
	}
	while (nb % k != 0 && nb != k)
	{
		k++;
	}
	if (nb % k == 0 && nb == k)
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
